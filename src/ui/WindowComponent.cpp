#include "WindowComponent.h"

/////////////////////////////////////////////////////////////////////////////////////////
WindowComponent& WindowComponent::Get()
{
  static WindowComponent instance;
  return instance;
}

/////////////////////////////////////////////////////////////////////////////////////////
bool WindowComponent::componentInitialize()
{
  return true;
}

/////////////////////////////////////////////////////////////////////////////////////////
void WindowComponent::setWindow(KonvergoWindow *window)
{
  connect(window, &KonvergoWindow::visibilityChanged,
          this, &WindowComponent::onVisibilityChanged);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
void WindowComponent::onVisibilityChanged(QWindow::Visibility visibility)
{
  emit visibilityChanged(visibility);
}
